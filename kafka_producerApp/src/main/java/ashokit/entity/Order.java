package ashokit.entity;

public class Order {
public String id;
public Double price;
public String email;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public Double getPrice() {
	return price;
}
public void setPrice(Double price) {
	this.price = price;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}

}
